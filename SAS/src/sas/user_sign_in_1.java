private void setCommonParameters(PreparedStatement pstmt) throws SQLException {
    String birthDate = cYearDate.getSelectedItem() + "-" +
            cMonthDate.getSelectedItem() + "-" +
            cDayDate.getSelectedItem();
    
    int paramIndex = 1;
    
    // School year
    pstmt.setString(paramIndex++, "2023-2024");
    
    // Section
    pstmt.setString(paramIndex++, cSection.getSelectedItem().toString());
    
    // Grade (assuming empty/null for new students)
    pstmt.setNull(paramIndex++, Types.VARCHAR);
    
    // Photo
    if (capturedImage != null) {
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        try {
            ImageIO.write(capturedImage, "jpg", baos);
            pstmt.setBytes(paramIndex++, baos.toByteArray());
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error processing image", "Error", JOptionPane.ERROR_MESSAGE);
            pstmt.setNull(paramIndex++, Types.BLOB);
        }
    } else {
        pstmt.setNull(paramIndex++, Types.BLOB);
    }
    
    // Campus
    pstmt.setString(paramIndex++, cCampus.getSelectedItem().toString());
    
    // Birth place
    pstmt.setString(paramIndex++, txtBirthPlace.getText());
    
    // Home address
    pstmt.setString(paramIndex++, txtAddress.getText());
    
    // Province
    pstmt.setString(paramIndex++, cProvince.getSelectedItem().toString());
    
    // City
    pstmt.setString(paramIndex++, cCity.getSelectedItem().toString());
    
    // Scheme
    pstmt.setString(paramIndex++, btnScheme1.isSelected() ? "Scheme 1" : "Scheme 2");
    
    // Barangay
    pstmt.setString(paramIndex++, txtBarangay.getText());
    
    // District
    pstmt.setString(paramIndex++, cDistrict.getSelectedItem().toString());
    
    // Sex
    pstmt.setString(paramIndex++, btnMale.isSelected() ? "Male" : "Female");
    
    // Civil status
    pstmt.setString(paramIndex++, cCivilStatus.getSelectedItem().toString());
    
    // Birth date
    pstmt.setDate(paramIndex++, java.sql.Date.valueOf(birthDate));
    
    // ZIP code
    pstmt.setString(paramIndex++, txtZip.getText());
    
    // Nationality
    pstmt.setString(paramIndex++, txtNationality.getText());
    
    // Religion
    pstmt.setString(paramIndex++, txtReligion.getText());
    
    // Contact no
    pstmt.setString(paramIndex++, txtContact.getText());
    
    // Mobile no
    pstmt.setString(paramIndex++, txtMobileNo.getText());
    
    // Height
    pstmt.setString(paramIndex++, txtHeight.getText());
    
    // Weight
    pstmt.setString(paramIndex++, txtWeight.getText());
    
    // Email
    pstmt.setString(paramIndex++, txtEmail.getText());
    
    // Password
    pstmt.setString(paramIndex++, txtPassword.getText());
    
    // Last name
    pstmt.setString(paramIndex++, txtlname.getText());
    
    // First name
    pstmt.setString(paramIndex++, txtfname.getText());
    
    // Middle name
    pstmt.setString(paramIndex++, txtmname.getText());
}
