    private void btnTakePhotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTakePhotoActionPerformed
        try {
            // Get default webcam
            webcam = Webcam.getDefault();

            if (webcam != null) {
                // Set HD resolution
                webcam.setViewSize(WebcamResolution.VGA.getSize());

                // Create webcam panel
                webcamPanel = new WebcamPanel(webcam);
                webcamPanel.setFPSDisplayed(true);
                webcamPanel.setDisplayDebugInfo(true);
                webcamPanel.setImageSizeDisplayed(true);
                webcamPanel.setMirrored(true);

                // Create a dialog to show the webcam feed
                JDialog dialog = new JDialog(this, "Take Photo", true);
                dialog.setLayout(new BorderLayout());
                dialog.add(webcamPanel, BorderLayout.CENTER);

                // Add capture button
                JButton captureButton = new JButton("Capture");
                captureButton.addActionListener(e -> {
                    // Capture image
                    capturedImage = webcam.getImage();

                    // Display in the preview label
                    if (capturedImage != null) {
                        ImageIcon icon = new ImageIcon(capturedImage);
                        Image scaledImage = icon.getImage().getScaledInstance(
                            photoPreview.getWidth(),
                            photoPreview.getHeight(),
                            Image.SCALE_SMOOTH);
                        photoPreview.setIcon(new ImageIcon(scaledImage));
                    }

                    // Close the dialog
                    dialog.dispose();
                });

                JPanel buttonPanel = new JPanel();
                buttonPanel.add(captureButton);
                dialog.add(buttonPanel, BorderLayout.SOUTH);

                // Set dialog properties
                dialog.pack();
                dialog.setLocationRelativeTo(this);
                dialog.setDefaultCloseOperation(JDialog.DISPOSE_ON_CLOSE);
                dialog.setVisible(true);

                // Clean up when dialog closes
                dialog.addWindowListener(new WindowAdapter() {
                    @Override
                    public void windowClosed(WindowEvent e) {
                        if (webcam != null) {
                            webcam.close();
                            webcam = null;
                        }
                        webcamPanel = null;
                    }
                });
            } else {
                JOptionPane.showMessageDialog(this,
                    "No webcam detected",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error accessing webcam: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
            if (webcam != null) {
                webcam.close();
                webcam = null;
            }
            webcamPanel = null;
        }
    }//GEN-LAST:event_btnTakePhotoActionPerformed

